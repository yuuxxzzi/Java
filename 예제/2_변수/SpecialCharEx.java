public class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\''); // ''' 처럼 적으면 안됨
        System.out.println("abc\t\123\b 456");
        System.out.println('\n');
        System.out.println("\"Hello\""); // " 출력방법
        System.out.println("c:\\"); // \ 출력
    }
}
